def print_dft(file,functional,cluster,basis,readmos):
        file.write('dft\n')
        file.write('  direct\n')
        if ( method == "hfx" ):
            file.write('  xc HFexch\n')
        else:
            file.write('  xc '+functional+'\n')
            # This is not the default grid but water clusters may require it for convergence.
            file.write('  grid fine\n')
        #file.write('  convergence energy 1e-9 density 1e-7 gradient 1e-7 lshift 0.2 rabuck 20\n')
        #file.write('  convergence energy 1e-7 density 1e-5 gradient 1e-5 lshift 0.2\n')
        #file.write('  convergence energy 1e-7 density 1e-5 gradient 1e-5\n')
        #file.write('  convergence energy 1e-9 density 1e-7 gradient 1e-7\n')
        file.write('  iterations 100\n')
        if readmos:
            file.write('  vectors input '+cluster+'_scf_'+basis+'.movecs\n')
        else:
            file.write('  vectors input atomic\n')

        file.write('  noprint "final vectors analysis" "final vector symmetries"\n')
        file.write('end\n\n')

def print_scf(file,method,cluster,basis,guessmos):
        file.write('scf\n')
        if ( method == "d-scf" ):
            file.write('  direct\n')
            #file.write('  level pcg 20 0.3 0.0 nr 0.2 0.005 0.0\n')
        else:
            # semidirect cache input uses units of words (1 word = 8 bytes).
            file.write('  semidirect memsize '+str(stack_mem*100000)+' filesize 0\n')
            #file.write('  nr 1000.0\n')
            #file.write('  nr 0.0\n')
            #file.write('  level pcg 20 0.3 0.0 nr 0.2 0.005 0.0\n')
        file.write('  singlet\n')
        file.write('  rhf\n')
        file.write('  thresh 1e-7\n')
        #file.write('  tol2e 1e-14\n')
        if guessmos:
            file.write('  vectors input atomic output '+cluster+'_scf_'+basis+'.movecs\n')

        file.write('  noprint "final vectors analysis" "final vector symmetries"\n')
        file.write('end\n\n')

def print_mp2(file):
        file.write('mp2\n')
        file.write('  freeze atomic\n')
        # MP2 scratchdisk given in units of MB.
        # 8 GB is the maximum one should use on most machines.
        file.write('  scratchdisk 8192\n')
        file.write('end\n\n')

def print_ccsd(file):
        file.write('ccsd\n')
        file.write('  freeze atomic\n')
        file.write('  thresh 1e-6\n')
        file.write('  maxiter 100\n')
        # On really fast machines (e.g. Nehalem), regenerating integrals is faster
        # than reading them from local disk unless those disks are SCSI (e.g. Chinook).
        if nodisk:
            file.write('  nodisk\n')
        file.write('end\n\n')
        file.write('#set ccsdt:memlimit 8000\n\n')
        file.write('#set ccsd:converged T\n\n')
        # Set to true to use OpenMP
        if openmp:
            file.write('set ccsd:use_ccsd_omp T\n')
            file.write('set ccsd:use_trpdrv_omp T\n\n')
        else:
            file.write('set ccsd:use_ccsd_omp F\n')
            file.write('set ccsd:use_trpdrv_omp T\n\n')

def print_tce(file,method):
        file.write('tce\n')
        file.write('  freeze atomic\n')
        file.write('  scf           \n')
        file.write('  '+method.replace("r", "").replace("-t","(t)")+'\n')
        file.write('  thresh 1e-6\n')
        file.write('  maxiter 100\n')
        if (method=='ccsd' or method=='ccsd(t)' or method=='ccsd-t'):
            file.write('  2eorb         \n')
            file.write('  2emet 13      \n')
            #file.write('  2emet 14      \n')
            #file.write('  split 4       \n')
        file.write('  attilesize 40\n')
        if ( method == 'ccsd(t)' or method == 'ccsd-t' or method == 'mp4sdq(t)' ):
            file.write('  tilesize 16\n')
        else:
            file.write('  tilesize 32\n')
        file.write('end\n\n')
        file.write('set tce:nts T\n\n')

